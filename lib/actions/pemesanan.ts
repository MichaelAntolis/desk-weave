export async function getOwnerStats() {
  const supabase = await createClient()
  const { data: { user } } = await supabase.auth.getUser()

  if (!user) return null

  // Get total revenue (DIKONFIRMASI + SELESAI bookings)
  const { data: confirmedBookings } = await supabase
    .from('pemesanan')
    .select('total_harga')
    .eq('pemilik_id', user.id)
    .in('status', ['DIKONFIRMASI', 'SELESAI'])

  const totalRevenue = (confirmedBookings || []).reduce((sum, b) => sum + b.total_harga, 0)

  // Get pending verification count
  const { count: pendingCount } = await supabase
    .from('pemesanan')
    .select('*', { count: 'exact', head: true })
    .eq('pemilik_id', user.id)
    .eq('status', 'MENUNGGU_VERIFIKASI')

  // Get total spaces
  const { count: totalSpaces } = await supabase
    .from('ruangan')
    .select('*', { count: 'exact', head: true })
    .eq('pemilik_id', user.id)

  // Get total bookings count (all statuses)
  const { count: totalBookings } = await supabase
    .from('pemesanan')
    .select('*', { count: 'exact', head: true })
    .eq('pemilik_id', user.id)

  // Get confirmed bookings count for occupancy calculation
  const { count: confirmedCount } = await supabase
    .from('pemesanan')
    .select('*', { count: 'exact', head: true })
    .eq('pemilik_id', user.id)
    .in('status', ['DIKONFIRMASI', 'SELESAI'])

  const occupancyRate = totalBookings && totalBookings > 0
    ? Math.round(((confirmedCount || 0) / totalBookings) * 100)
    : 0

  return {
    totalRevenue,
    pendingCount: pendingCount || 0,
    totalSpaces: totalSpaces || 0,
    totalBookings: totalBookings || 0,
    confirmedCount: confirmedCount || 0,
    occupancyRate,
  }
}

/**
 * HELPER: Memastikan URL gambar valid dan menunjuk ke bucket foto-ruangan
 * Versi ULTRA-RESILIENT: Menggunakan hardcoded Project ID jika ENV gagal
 */
function sanitizeImageUrl(url: string | null): string {
  const fallback = "https://images.unsplash.com/photo-1497366216548-37526070297c?w=1200&q=80";
  if (!url || url === '' || url === 'undefined' || url === 'null') return fallback;
  
  const PROJECT_ID = "abvpmrfdbuccbheuovvv";
  const HARDCODED_BASE = `https://${PROJECT_ID}.supabase.co`;
  let supabaseUrl = (process.env.NEXT_PUBLIC_SUPABASE_URL || HARDCODED_BASE).trim();
  
  supabaseUrl = supabaseUrl.replace(/['"]/g, '');
  if (supabaseUrl.endsWith('/')) supabaseUrl = supabaseUrl.slice(0, -1);
  
  if (url.includes('supabase.co/storage/v1/object/public/')) {
    const parts = url.split('/public/');
    const bucketAndPath = parts[parts.length - 1];
    return `${supabaseUrl}/storage/v1/object/public/${bucketAndPath}`;
  } 
  
  if (url.startsWith('http')) return url;

  let path = url.replace(/\/+/g, '/');
  if (path.startsWith('/')) path = path.substring(1);
  return `${supabaseUrl}/storage/v1/object/public/foto-ruangan/${path}`;
}

export async function getOwnerRuangan() {
  const supabase = await createClient()
  const { data: { user } } = await supabase.auth.getUser()

  if (!user) return []

  const { data, error } = await supabase
    .from('ruangan')
    .select(`
      *,
      ruangan_fasilitas(
        fasilitas(*)
      ),
      foto_ruangan(*)
    `)
    .eq('pemilik_id', user.id)
    .order('dibuat_pada', { ascending: false })

  if (error) {
    console.error('Error fetching owner ruangan:', error)
    return []
  }

  return (data || []).map((ruangan) => {
    const r = {
      ...ruangan,
      fasilitas: (ruangan.ruangan_fasilitas || []).map((rf: { fasilitas: unknown }) => rf.fasilitas).filter(Boolean),
      ruangan_fasilitas: undefined,
    }
    // Sanitize images
    r.foto_utama = sanitizeImageUrl(r.foto_utama);
    if (r.foto_ruangan) {
      r.foto_ruangan = r.foto_ruangan.map((f: any) => ({
        ...f,
        url: sanitizeImageUrl(f.url)
      }))
    }
    return r
  })
}

