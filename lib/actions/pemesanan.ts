export async function getOwnerPemesanan(status?: StatusPemesanan) {
  const supabase = await createClient()
  const { data: { user } } = await supabase.auth.getUser()

  if (!user) return []

  let query = supabase
    .from('pemesanan')
    .select(`
      *,
      ruangan(id, nama, lokasi, foto_utama),
      profil:penyewa_id(id, nama_lengkap, email, avatar_url)
    `)
    .eq('pemilik_id', user.id)
    .order('dibuat_pada', { ascending: false })

  if (status) {
    query = query.eq('status', status)
  }

  const { data, error } = await query

  if (error) {
    console.error('Error fetching owner pemesanan:', error)
    return []
  }

  return (data || []).map((p) => ({
    ...p,
    penyewa: p.profil,
    profil: undefined,
  }))
}

export async function verifyPemesanan(pemesananId: string, action: 'approve' | 'reject', catatan?: string) {
  const supabase = await createClient()
  const { data: { user } } = await supabase.auth.getUser()

  if (!user) {
    return { error: 'Tidak terautentikasi' }
  }

  const newStatus: StatusPemesanan = action === 'approve' ? 'DIKONFIRMASI' : 'DITOLAK'

  const updateData: Record<string, unknown> = {
    status: newStatus,
    diverifikasi_pada: new Date().toISOString(),
  }

  if (catatan) {
    updateData.catatan_pemilik = catatan
  }

  const { error } = await supabase
    .from('pemesanan')
    .update(updateData)
    .eq('id', pemesananId)
    .eq('pemilik_id', user.id) // Ensure the user is the owner

  if (error) {
    return { error: error.message }
  }

  return { success: true }
}

