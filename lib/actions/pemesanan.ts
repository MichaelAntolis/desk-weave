export async function getOwnerCalendarData() {
  const supabase = await createClient()
  const { data: { user } } = await supabase.auth.getUser()

  if (!user) return { spaces: [], bookings: [] }

  // Get all spaces owned by the user
  const { data: spaces, error: spacesError } = await supabase
    .from('ruangan')
    .select('id, nama, tipe')
    .eq('pemilik_id', user.id)

  if (spacesError) {
    console.error('Error fetching calendar spaces:', spacesError)
    return { spaces: [], bookings: [] }
  }

  // Get all bookings for these spaces
  const { data: bookings, error: bookingsError } = await supabase
    .from('pemesanan')
    .select(`
      id,
      ruangan_id,
      tanggal_mulai,
      tanggal_selesai,
      jam_mulai,
      jam_selesai,
      status,
      total_harga,
      ruangan(nama)
    `)
    .eq('pemilik_id', user.id)
    .in('status', ['DIKONFIRMASI', 'SELESAI', 'MENUNGGU_VERIFIKASI', 'PENDING'])

  if (bookingsError) {
    console.error('Error fetching calendar bookings:', bookingsError)
    return { spaces: spaces || [], bookings: [] }
  }

  return {
    spaces: spaces || [],
    bookings: bookings || [],
  }
}

