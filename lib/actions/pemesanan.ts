  const { data: { user } } = await supabase.auth.getUser()

  if (!user) return [] as TenantBooking[]

  const { data, error } = await supabase
    .from('pemesanan')
    .select(`
      *,
      ruangan(id, nama, lokasi, kota, foto_utama, harga_per_hari)
    `)
